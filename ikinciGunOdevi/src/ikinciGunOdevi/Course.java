package ikinciGunOdevi;

public class Course {

	int id;
	String name;
	String courseTeacher;
	double completion;

	public Course(int id, String name, String courseTeacher, double completion) {
		super();
		this.id = id;
		this.name = name;
		this.courseTeacher = courseTeacher;
		this.completion = completion;
	}

}
